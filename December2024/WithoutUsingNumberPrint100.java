                 public class WithoutUsingNumberPrint100 {
    public static void main(String[] args) {
        int one = 'b' / 'b'; // 'b' ASCII value is 98, so 'b'/'b' = 1
        int hundred = 'd';  // 'd' ASCII value is 100

        for (int i = one; i <= hundred; i++) {
            System.out.println("Without using the number: " + i);
        }
    }
}

